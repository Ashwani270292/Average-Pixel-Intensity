public class GreyColor {

    private long intensityCount;
    private long pixelCount;

    public long getIntensityCount() {
        return intensityCount;
    }

    public void setIntensityCount(long intensityCount) {
        this.intensityCount = intensityCount;
    }

    public long getPixelCount() {
        return pixelCount;
    }

    public void setPixelCount(long pixelCount) {
        this.pixelCount = pixelCount;
    }
}
